const NAV_ITEMS = [
  { href: "/",          Icon: HomeIcon,   label: "ホーム",     match: ["/"] },
  { href: "/search",    Icon: SearchIcon, label: "さがす",     match: ["/search", "/results"] },
  { href: "/favorites", Icon: HeartIcon,  label: "お気に入り", match: ["/favorites"] },
  { href: "/guide",     Icon: BookIcon,   label: "ガイド",     match: ["/guide"] },
] as const;

export default function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 flex items-stretch justify-around z-20"
      style={{ paddingBottom: "max(0px, env(safe-area-inset-bottom))" }}
    >
      {NAV_ITEMS.map((item) => {
        const isActive =
          item.href === "/" ? pathname === "/" : item.match.some((p) => pathname.startsWith(p));
        return (
          <Link
            key={item.href}
            href={item.href}
            aria-label={item.label}
            aria-current={isActive ? "page" : undefined}
            className={`flex flex-col items-center justify-center gap-0.5 py-2 px-2 flex-1 min-h-[52px] transition-colors ${
              isActive ? "text-[#BF0000]" : "text-gray-500"
            }`}
          >
            <item.Icon filled={isActive} />
            <span className={`text-[11px] ${isActive ? "font-bold text-[#BF0000]" : "font-normal"}`}>
              {item.label}
            </span>
          </Link>
        );
      })}
    </nav>
  );
}
